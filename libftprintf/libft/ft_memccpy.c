#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	a;

	a = (unsigned char)(c);
	i = 0;
	while (i < n)
	{
		*(unsigned char *)(dst + i) = *(unsigned char *)(src + i);
		if (*(unsigned char *)(src + i) == a)
		{
			i++;
			return (dst + i);
		}
		i++;
	}
	return (NULL);
}
