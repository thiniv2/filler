#include "libft.h"

void	ft_putnbr(int n)
{
	long	k;

	k = n;
	if (n < 0)
	{
		ft_putchar('-');
		k = k * -1;
	}
	if (k / 10 > 0)
	{
		ft_putnbr(k / 10);
	}
	ft_putchar('0' + k % 10);
}
